drawStickFigure :: Int -> String
drawStickFigure x = unlines $ case x of
    0 -> ["=========", "|    |", "|   ", "|   ", "|   "]
    1 -> ["=========",
          "|    |",
          "|   " ++ " O ",
          "|   ",
          "|   "]
    2 -> ["=========",
          "|    |",
          "|   " ++ " O ",
          "|   " ++ " |  "]
    3 -> ["=========",
          "|    |",
          "|   " ++ " O ",
          "|   " ++ " | ",
          "|   " ++ "/  "]
    4 -> ["=========",
          "|    |",
          "|   " ++ " O ",
          "|   " ++ " | ",
          "|   " ++ "/ \\"]
    5 -> ["=========",
          "|    |",
          "|   " ++ "_O ",
          "|   " ++ " | ",
          "|   " ++ "/ \\"]
    6 -> ["=========",
          "|    |",
          "|   " ++ "_O_",
          "|   " ++ " | ",
          "|   " ++ "/ \\"]          

main = putStr (drawStickFigure 0)